/**
 * Returns the topic list for a given subject ID.
 * @param {string} subjectId  e.g. 'chemistry_s1', 'maths_y7', 'english_y7'
 * @returns {{ code: string, name: string }[]}
 */
export function getTopicsBySubject(subjectId) {
  switch (subjectId) {
    case 'chemistry_s1':
    case 'Chemistry Stage 1': return S1_TOPICS
    case 'chemistry_s2':
    case 'Chemistry Stage 2': return S2_TOPICS
    case 'maths_y7':
    case 'Year 7 Mathematics': return Y7_MATHS_TOPICS
    case 'english_y7':
    case 'Year 7 English': return Y7_ENGLISH_TOPICS
    case 'maths_y10':
    case 'Year 10 Mathematics': return Y10_MATHS_TOPICS
    default: return S1_TOPICS
  }
}

/**
 * @param {'s1'|'s2'} stage
 * @param {string} code  e.g. '2.2'
 * @returns {{ code: string, name: string } | null}
 */
export function getTopicByCode(stage, code) {
  const list = stage === 's1' ? S1_TOPICS : S2_TOPICS
  return list.find(t => t.code === code) ?? null
}

/**
 * Returns the topic list as a numbered string for use in AI prompts.
 * @param {'s1'|'s2'} stage
 * @returns {string}
 */
export function topicsAsPromptList(stage) {
  const list = stage === 's1' ? S1_TOPICS : S2_TOPICS
  return list.map(t => `${t.code}: ${t.name}`).join('\n')
}
